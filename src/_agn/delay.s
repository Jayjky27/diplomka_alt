	.syntax unified

	.global delay

/*
 *==============================================================================
 * delay
 *==============================================================================
 */

	.section .text.delay
	.type delay, %function
	.thumb_func
delay:

	subs r0, #1
	bne delay
	bx lr

	.size delay, . - delay

/*
 *==============================================================================
 * EOF
 *==============================================================================
 */
	.end
